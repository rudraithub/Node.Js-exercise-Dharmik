// ## 0501- Write a program to check whether a number is positive, negative or zero using switch case.

let a=23

switch(true){
    case   a>=0 :     
        console.log("positive")
        break
    
    case  a<0 : 
        console.log("negative")
        break
    
    default : 
                console.log("please provide valid input")
             
}

